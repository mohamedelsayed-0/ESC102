              'BackgroundColor', [0.25 0.27 0.30], 'ForegroundColor', [0.95 0.95 1]);
    yPos = yPos - deltaY;

    % Strap length range
    uicontrol('Parent', controlPanel, 'Style', 'text', ...
              'String', 'Range of Lengths (cm):', ...
              'Units', 'normalized', 'Position', [0.05 yPos 0.6 0.04], ...
              'HorizontalAlignment', 'left', 'BackgroundColor', [0.18 0.20 0.22], ...
